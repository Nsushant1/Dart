void main() {
  // ##null variable
  //Null is a special value in Dart that represents the absence of a value.
  //In Dart, null is a type that can be assigned to any variable.

  String? name;
  //? is used to declare a nullable variable.
  print(name);

  name = "Sushatnt";
  print(name);
  name = ' 56';
  print(name.length);

  name = null;
  print(name!.length);
}
